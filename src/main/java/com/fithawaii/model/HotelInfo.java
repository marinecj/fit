package com.fithawaii.model;

public class HotelInfo {

	private int hotelNo;
	private String hotelNm;
	private String hotelNmKr;
	private String nat;
	private String state;
	private String stateKr;
	private String country;
	private String hotelAddress;
	private String hotelTel;
	private String hotelUrl;

	public int getHotelNo() {
		return hotelNo;
	}

	public void setHotelNo(int hotelNo) {
		this.hotelNo = hotelNo;
	}

	public String getHotelNm() {
		return hotelNm;
	}

	public void setHotelNm(String hotelNm) {
		this.hotelNm = hotelNm;
	}

	public String getHotelNmKr() {
		return hotelNmKr;
	}

	public void setHotelNmKr(String hotelNmKr) {
		this.hotelNmKr = hotelNmKr;
	}

	public String getNat() {
		return nat;
	}

	public void setNat(String nat) {
		this.nat = nat;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getStateKr() {
		return stateKr;
	}

	public void setStateKr(String stateKr) {
		this.stateKr = stateKr;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getHotelAddress() {
		return hotelAddress;
	}

	public void setHotelAddress(String hotelAddress) {
		this.hotelAddress = hotelAddress;
	}

	public String getHotelTel() {
		return hotelTel;
	}

	public void setHotelTel(String hotelTel) {
		this.hotelTel = hotelTel;
	}

	public String getHotelUrl() {
		return hotelUrl;
	}

	public void setHotelUrl(String hotelUrl) {
		this.hotelUrl = hotelUrl;
	}
}
